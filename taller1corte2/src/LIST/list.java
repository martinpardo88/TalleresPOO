     private void agregarProducto() {
        String producto = jTextField1.getText().trim();
        if (!producto.isEmpty()) {
            listModel.addElement(producto); 
            jTextField1.setText(""); 
        }
    }

    private void eliminarProducto() {
        int selectedIndex = jList1.getSelectedIndex(); 
        if (selectedIndex != -1) {
            listModel.remove(selectedIndex); 
        }
    } 
    
    
    
    
    
